"""Example module for PQuantomatic library."""


def hello():
    """Return a greeting message.
    
    Returns:
        str: A greeting message.
    """
    return "Hello from PQuantomatic!"


def add(a, b):
    """Add two numbers.
    
    Args:
        a: First number
        b: Second number
        
    Returns:
        The sum of a and b
    """
    return a + b
